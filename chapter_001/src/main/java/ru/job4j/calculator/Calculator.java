package ru.job4j.calculator;

/**
 * Calculator.
 * @author @author Tikhonov Yaroslav (eexxyyq@yandex.ru).
 */
public class Calculator {
    /**
     * Method add.
     * @param first
     * @param second
     * @return first + second.
     */
    public double add(double first, double second) {
        return first + second;
    }

    /**
     * Method div.
     * @param first
     * @param second
     * @return first / second
     */
    public double div(double first, double second) {
        return first / second;
    }

    /**
     * Method substact.
     * @param first
     * @param second
     * @return first - second
     */
    public double substract(double first, double second) {
        return first - second;
    }

    /**
     * Method multiplie.
     * @param first
     * @param second
     * @return first * second
     */
    public double multiple(double first, double second) {
        return first * second;
    }
}